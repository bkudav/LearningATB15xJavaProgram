package ex_01_Java_Basics;

public class Lab17_nextline_tab {

    public static void main(String[] args) {
        int a = 100;
        int b = 200;

        System.out.println("Display value of a:\t" + a);
        System.out.println("Display value of a:\t" + b);

        System.out.println("Display value of a:\t\n" + a);
        System.out.println("Display value of a:\t" + b);

    }
}

