package ex_11_ForLoop;

import java.util.Scanner;

public class Lab62_Countdown_BlastOff {
    public static void main(String[] args) {


        //For Loop
        for (int i=10;i>=1;i--)
            System.out.println(i);
        System.out.println("Blast Off");
        //print the word "Blast off!
    }
}
