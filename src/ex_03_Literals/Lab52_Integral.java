package ex_03_Literals;

public class Lab52_Integral {
    public static void main(String[] args) {
        //Integral Literals in Java
        int a = 101; //Decimal Integral. Base-10
        int b = 0123; //Octal Integral. Base-8
        int c = 0x123Face; //Hexa-decimal Integral. Base-16. 0-9 and A-F/a-f
        int d = 0b1010; //Binary literal

        //Floating-Point Literal in Java
        double e = 123.123; //Decimal Floating-Point Literal

        //Char Literals in Java
        char ch = 'a';

        //String Literals in Java
        String s = "Hello";
        String text = "This is a String literal\n";

        //Boolean Literals in Java
        boolean f = true;

        boolean g = false;

        //Display all  the literals (values)
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);




    }
}
